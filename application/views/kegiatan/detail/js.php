                className: 'text-right',
                targets: [2]
            }, {
                className: 'text-center',
                targets: [0, 1, 3]
            }],
            columns: [{
                data: 'no'
            }, {
                data: 'tgl_realisasi'
            }, {
                data: 'nilai'
            }, {
                data: 'keterangan'
            }, {
                data: 'aksi'
            }]
        });
    }

    function load_data_dokumentasi() {
        $('#display-dokumentasi').css('display', 'none');
        $('.preload_dokumentasi').show();
        $.ajax({
            url: '<?= site_url('kegiatan/dokumentasi/load_dk'); ?>',
            type: "POST",
            data: {
                id_kontrak: "<?= $ta_kontrak['id_kontrak']; ?>"
            },
            success: function(data) {
                $('#display-dokumentasi').css('display', 'block');
                $('.preload_dokumentasi').hide();
                $('#dokumentasi').html(data);
            }
        });
    }

    function reload_ajax() {
        table.ajax.reload(null, false);
        table_kontrak.ajax.reload(null, false);
    }

    $(document).on("click", "#tombol-realisasi", function() {
        var self = this;
        $(self).attr('disabled', true);
        $(self).html("<i class='fa fa-circle-notch fa-spin fa-sm'></i>");
        $.ajax({
            url: '<?= site_url('kegiatan/detail/form'); ?>',
            type: "POST",
            data: {
                opsi: "realisasi",
                id: $(this).data('id')
            },
            success: function(data) {
                $('#load-form-action').html(data);
                $(self).html('<i class="fa fa-edit"></i> Ubah Realisasi');
                $(self).attr('disabled', false);
            }
        });
    });

    $(document).on("submit", "#form-realisasi", function(e) {
        e.preventDefault(e);
        var self = "#btn-realisasi";
        var form_id = "#form-realisasi";
        $(self).attr('disabled', true);
        $(self).html("<i class='fa fa-circle-notch fa-spin fa-sm'></i> LOADING...");
        $.ajax({
            url: "<?= site_url('kegiatan/detail/realisasi'); ?>",
            type: 'POST',
            data: $(form_id).serialize(),
            dataType: "json",
            success: function(data) {
                if (data.status) {
                    $('#modal-form-action').modal('hide');
                    reload_ajax();
                    if (data.notif) {
                        notifikasi('success', 'Berhasil', 'Data Berhasil Disimpan');
                    } else {
                        notifikasi('error', 'Gagal', 'Data Gagal Disimpan');
                    }
                } else {
                    notifikasi('error', 'Gagal', data.pesan);
                    $.each(data.errors, function(key, value) {
                        $(form_id + ' [name="' + key + '"]').parents(".form-group").removeClass('has-success');
                        $(form_id + ' [name="' + key + '"]').parents(".form-group").addClass('has-error');
                        $(form_id + ' .' + key).html(value);
                        if (value == "") {
                            $(form_id + ' [name="' + key + '"]').parents(".form-group").removeClass('has-error');
                            $(form_id + ' [name="' + key + '"]').parents(".form-group").addClass('has-success');
                        }
                    });
                }
                $(self).html("<i class='fa fa-save'></i> SIMPAN");
                $(self).attr('disabled', false);
            },
            error: function(xhr, status, msg) {
                alert('Status: ' + status + "\n" + msg);
                $(self).html("<i class='fa fa-save'></i> SIMPAN");
                $(self).attr('disabled', false);
            }
        });
    });

