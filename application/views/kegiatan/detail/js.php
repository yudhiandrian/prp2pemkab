    function load_data() {
        $('#display-content').css('display', 'none');
        $('.preload').show();
        table = $('#load-content').DataTable({
            destroy: true,
            ordering: false,
            bAutoWidth: false,
            initComplete: function() {
                $('#display-content').css('display', 'block');
                $('.preload').hide();
            },
            ajax: {
                url: "<?= site_url('kegiatan/detail/load'); ?>",
                type: 'POST',
                data: {
                    id_kontrak: "<?= $ta_kontrak['id_kontrak']; ?>"
                }
            },
            columnDefs: [{
                className: 'text-right',
                targets: [2, 4]
            }, {
                className: 'text-center',
                targets: [0, 3, 3, 5, 6]
            }],
            columns: [{
                data: 'no'
            }, {
                data: 'tahapan_target'
            }, {
                data: 'target'
            }, {
                data: 'realisasi'
            }, {
                data: 'persen_deviasi'
            }, {
                data: 'keterangan_target'
            }, {
                data: 'opsi'
            }, {
                data: 'aksi'
            }]
        });

        table_kontrak = $('#load-kontrak').DataTable({
            destroy: true,
            ordering: false,
            bAutoWidth: false,
            searching: false,
            info: false,
            paging: false,
            initComplete: function() {
                $('#display-content').css('display', 'block');
                $('.preload').hide();
            },
            ajax: {
                url: "<?= site_url('kegiatan/kontrak/load'); ?>",
                type: 'POST',
